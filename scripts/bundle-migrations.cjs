/**
 * Writes migrations/_bundle_all.sql — numbered 001–*.sql files concatenated in order
 * for a single paste into Supabase SQL Editor when no DATABASE_URL is available.
 */
const fs = require("fs");
const path = require("path");

const migrationsDir = path.join(__dirname, "..", "migrations");
const outFile = path.join(migrationsDir, "_bundle_all.sql");

const files = fs
  .readdirSync(migrationsDir)
  .filter((f) => /^\d{3}_/.test(f) && f.endsWith(".sql"))
  .sort();

let out = `-- Bundled migrations (001–012). Generated by scripts/bundle-migrations.cjs\n`;
out += `-- Run in Supabase → SQL Editor if you are not using npm run db:migrate\n\n`;

for (const f of files) {
  const sql = fs.readFileSync(path.join(migrationsDir, f), "utf8");
  out += `-- ========== ${f} ==========\n\n`;
  out += sql.trim();
  out += `\n\n`;
}

fs.writeFileSync(outFile, out, "utf8");
console.log(`Wrote ${path.relative(process.cwd(), outFile)} (${files.length} files).`);
